<#  #### WHY POWERSHELL? ####

In what situations can a C# developer benefit by faking Powershell experience?

* You're too lazy to build a full admin tool for your project, but you want to dump
  responsibilities from your app dev team to devops

* Boss is more likely to let you build a Powershell script for your pet project
  than a full blown C# app.  (I snuck this past two bosses)
 
* Boss asks "Does anybody know Powershell?" and you want to look good

* You have to do some serious operating system level devops stuff 
  (proccesses, memory, disk space, networking)

* You're doing anything with AWS, Azure, Ansible, ActiveDirectory, Sharepoint admin/automation, 
  TFS build automation

